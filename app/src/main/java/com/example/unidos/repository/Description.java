package com.example.unidos.repository;

public class Description {
    private float height;
    private int comp;
    private int skin;
    private int lips;
    private int eyeColor;
    private int eyeShape;
    private int face;
    private int nose;
    private int hairShape;
    private int hairColor;
    private String features;

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public int getComp() {
        return comp;
    }

    public void setComp(int comp) {
        this.comp = comp;
    }

    public int getSkin() {
        return skin;
    }

    public void setSkin(int skin) {
        this.skin = skin;
    }

    public int getLips() {
        return lips;
    }

    public void setLips(int lips) {
        this.lips = lips;
    }

    public int getEyeColor() {
        return eyeColor;
    }

    public void setEyeColor(int eyeColor) {
        this.eyeColor = eyeColor;
    }

    public int getEyeShape() {
        return eyeShape;
    }

    public void setEyeShape(int eyeShape) {
        this.eyeShape = eyeShape;
    }

    public int getFace() {
        return face;
    }

    public void setFace(int face) {
        this.face = face;
    }

    public int getNose() {
        return nose;
    }

    public void setNose(int nose) {
        this.nose = nose;
    }

    public int getHairShape() {
        return hairShape;
    }

    public void setHairShape(int hairShape) {
        this.hairShape = hairShape;
    }

    public int getHairColor() {
        return hairColor;
    }

    public void setHairColor(int hairColor) {
        this.hairColor = hairColor;
    }

    public String getFeatures() {
        return features;
    }

    public void setFeatures(String features) {
        this.features = features;
    }
}
